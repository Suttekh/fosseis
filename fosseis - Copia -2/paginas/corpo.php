    <div style="font-family: garoto;" ><h1>Pesquisa apresenta réptil raro que conviveu com dinossauros em Agudo</h1></div>

    <p>Professor de Santa Maria encontrou primeiro fóssil de um ornitossuquídeo, antepassado do crocodilo, no ano passado. Antes deste, existiam somente três exemplares destes animais no mundo.</p>
