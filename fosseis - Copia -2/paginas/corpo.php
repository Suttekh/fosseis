<div class="text-left" style="font-family: Georgia, 'Times New Roman', Times, serif;" ><h5>A descoberta de um réptil pré-histórico, que conviveu com os dinossauros e é antepassado dos crocodilos, foi apresentada em um estudo publicado em 31 de janeiro pela revista científica internacional Acta Paleontologica Polonica.

O fóssil é de um Dynamosuchus collisensis, da família dos ornitossuquídeos, e foi localizado em Agudo, na Região Central do RS, em março do ano passado, por Rodrigo Temp Müller, paleontólogo da Universidade de Santa Maria (UFSM). Assinam também o artigo o Museu de La Plata, na Argentina, e a universidade de Virginia Tech, nos Estados Unidos.

O ornitossuquídeo viveu há cerca de 230 milhões de anos, e, conforme Müller, é uma descoberta rara, de uma espécie que conta com pouco material de estudo.</h5></div>

<h3>"No mundo todo, só tinha três espécies, duas na Argentina e uma na Escócia. É mais uma das descobertas mostrando o potencial fossilífero que a gente tem, não só de dinossauros, mas de vários outros tipo de répteis, incluindo esse grupo do ornitossuquídeo, que é a primeira vez aqui no Brasil", ressalta.</h3>
<br>

<div style="font-family: Georgia, 'Times New Roman', Times, serif;" ><h5>O exemplar ainda ajuda a entender melhor como se davam as relações entre as espécies da época, já que, como explica o professor, o ornitossuquídeo seria um animal negrófago, ou seja, se alimentava de carcaças.</h5></div>
   

